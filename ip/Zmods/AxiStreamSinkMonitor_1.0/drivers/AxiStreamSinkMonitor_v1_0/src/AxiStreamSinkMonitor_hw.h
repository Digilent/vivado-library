#define AXI_STREAM_SINK_MONITOR_CONTROL_SELECTVOID_MASK 0x4
/* STATUS register bitfields */
#define AXI_STREAM_SINK_MONITOR_STATUS_IDLE_MASK 0x1
/* NUMFRAMES register bitfields */
#define AXI_STREAM_SINK_MONITOR_NUMFRAMES_NUMFRAMES_MASK 0xffffffff
/* BEATCOUNT register bitfields */
#define AXI_STREAM_SINK_MONITOR_BEATCOUNT_BEATCOUNT_MASK 0xffffffff
/* MISSCOUNT register bitfields */
#define AXI_STREAM_SINK_MONITOR_MISSCOUNT_MISSCOUNT_MASK 0xffffffff
/* ERRORCOUNT register bitfields */
#define AXI_STREAM_SINK_MONITOR_ERRORCOUNT_ERRORCOUNT_MASK 0xffffffff

#endif /* end of protection macro */
