#define AXI_STREAM_SINK_MONITOR_IP_INTR_EN_AP_DONE_MASK 0x1
#define AXI_STREAM_SINK_MONITOR_IP_INTR_EN_AP_READY_MASK 0x2
#define AXI_STREAM_SINK_MONITOR_IP_INTR_EN_ALL_MASK 0x3

/* IP interrupt status register bitfields */
#define AXI_STREAM_SINK_MONITOR_IP_INTR_STS_AP_DONE_MASK 0x1
#define AXI_STREAM_SINK_MONITOR_IP_INTR_STS_AP_READY_MASK 0x2
/* CONTROL register bitfields */
#define AXI_STREAM_SINK_MONITOR_CONTROL_START_MASK 0x1
